#include "libft.h"

void	ft_putendl(const char *s)
{
	int	i;

	if ((unsigned char *)(s))
	{
		i = 0;
		while (s[i])
		{
			write(1, &s[i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
}
