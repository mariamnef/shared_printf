#include "libft.h"

char	*ft_strtoupper(char *s)
{
	size_t	i;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i] >= 97 && s[i] <= 122)
	{
		s[i] = s[i] - 32;
		i++;
	}
	return (s);
}
